Дан массив с элементами 1, 2, 3, 4, 5, 6, 7, 8, 9. С помощью цикла foreach и оператора if
выведите на экран столбец элементов массива, как показано на картинке.<br>
1, 2, 3<br>
4, 5, 6<br>
7, 8, 9</p>
<?php
$br = "<br>";
$comma = ',';
$arr = [1, 2, 3, 4, 5, 6, 7, 8, 9];
foreach ($arr as $i){
    if ($i%3==0){
        echo $i . $br;
    }
    else{
        echo $i . $comma;
    }
}
?>